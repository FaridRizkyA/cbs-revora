const loadNativeFileExportModules = async () => {
  const fileSystemModule = await import("expo-file-system");
  return {
    Directory: fileSystemModule.Directory,
    File: fileSystemModule.File,
  };
};

const splitFileName = (fileName: string) => {
  const dotIndex = fileName.lastIndexOf(".");
  if (dotIndex <= 0) {
    return { name: fileName, extension: "" };
  }

  return {
    name: fileName.slice(0, dotIndex),
    extension: fileName.slice(dotIndex),
  };
};

const getAvailableFileName = (
  directory: Awaited<ReturnType<typeof loadNativeFileExportModules>>["Directory"]["prototype"],
  fileName: string
) => {
  const existingNames = new Set(directory.list().map((item) => item.name));
  if (!existingNames.has(fileName)) {
    return fileName;
  }

  const { name, extension } = splitFileName(fileName);
  for (let index = 1; index < 1000; index += 1) {
    const candidate = `${name} (${index})${extension}`;
    if (!existingNames.has(candidate)) {
      return candidate;
    }
  }

  return `${name}-${Date.now()}${extension}`;
};

const downloadBackendExcel = async ({
  title,
  reportKey,
  generatedAt,
  generatedBy,
  meta = [],
  columns,
  rows,
  fileName,
}: {
  title: string;
  reportKey: string;
  generatedAt?: string | Date;
  generatedBy?: string | null;
  meta?: ExcelMetaItem[];
  columns: { key: string; title: string; width?: number; align?: "left" | "center" | "right" }[];
  rows: Record<string, ExcelValue>[];
  fileName?: string;
}) => {
  if (Platform.OS === "web") return false;

  const targetFileName = fileName || buildExcelFileName(reportKey, generatedAt);
  const response = await fetchWithAuth(`/api/reports/export-excel`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      title,
      file_name: targetFileName,
      columns,
      rows,
      meta: [
        ...(!meta.some(m => m.label === "Generated At") ? [{ label: "Generated At", value: formatDateTime(generatedAt) }] : []),
        ...(generatedBy && !meta.some(m => m.label === "Generated By") ? [{ label: "Generated By", value: generatedBy }] : []),
        ...(!meta.some(m => m.label === "Total Rows") ? [{ label: "Total Rows", value: rows.length }] : []),
        ...meta,
      ],
    }),
  });

  if (!response.ok) {
    const payload = await response.json().catch(() => null);
    throw new Error(payload?.message || "Failed to export Excel.");
  }

  const fileBytes = new Uint8Array(await response.arrayBuffer());
  const { Directory } = await loadNativeFileExportModules();
  const targetDirectory = await Directory.pickDirectoryAsync();
  const availableFileName = getAvailableFileName(targetDirectory, targetFileName);
  const targetFile = targetDirectory.createFile(
    availableFileName,
    "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
  );
  targetFile.write(fileBytes);
  return true;
};

const toExcelCellValue = (value: ExcelValue) => {
  if (value instanceof Date) return value;
  if (typeof value === "number") return value;
  if (typeof value === "boolean") return value ? "Yes" : "No";
  if (value === null || value === undefined || value === "") return "-";
  return String(value);
};

export const downloadExcelWorkbook = async <Row,>(options: ExcelTableOptions<Row>) => {
  const handledByBackend = await downloadBackendExcel({
    title: options.title,
    reportKey: options.reportKey,
    generatedAt: options.generatedAt,
    generatedBy: options.generatedBy,
    meta: options.meta,
    fileName: options.fileName,
    columns: options.columns.map((column) => ({
      key: column.key,
      title: column.title,
      width: column.width,
      align: column.align,
    })),
    rows: options.rows.map((row, rowIndex) =>
      Object.fromEntries(options.columns.map((column) => [column.key, column.getValue(row, rowIndex)]))
    ),
  });
  if (handledByBackend) return;

  const ExcelJS = await loadExcelJs();
  const workbook = new ExcelJS.Workbook();
  workbook.creator = "CBS Revora";
  workbook.created = new Date();
  const worksheet = workbook.addWorksheet(options.title.slice(0, 31) || "Report");
  const columns = options.columns || [];
  const columnCount = Math.max(columns.length, 1);

  worksheet.columns = columns.map((column) => ({
    key: column.key,
    width: column.width || 18,
  }));

  const cbsLogoBuffer = await assetToArrayBuffer(CBS_LOGO);
  const revoraLogoBuffer = await assetToArrayBuffer(REVORA_LOGO);
  if (cbsLogoBuffer) {
    const cbsLogoId = workbook.addImage({ buffer: cbsLogoBuffer, extension: "png" });
    worksheet.addImage(cbsLogoId, { tl: { col: 0, row: 0 }, ext: { width: 52, height: 52 } });
  }
  if (revoraLogoBuffer) {
    const revoraLogoId = workbook.addImage({ buffer: revoraLogoBuffer, extension: "png" });
    worksheet.addImage(revoraLogoId, { tl: { col: 1, row: 0.15 }, ext: { width: 190, height: 44 } });
  }

  worksheet.mergeCells(1, Math.max(columnCount - 2, 1), 1, columnCount);
  worksheet.mergeCells(2, Math.max(columnCount - 2, 1), 2, columnCount);
  worksheet.getCell(1, Math.max(columnCount - 2, 1)).value = options.title;
  worksheet.getCell(1, Math.max(columnCount - 2, 1)).font = { bold: true, size: 20, color: { argb: "FF0F2852" } };
  worksheet.getCell(1, Math.max(columnCount - 2, 1)).alignment = { horizontal: "right", vertical: "middle" };
  worksheet.getCell(2, Math.max(columnCount - 2, 1)).value = options.subtitle || "";
  worksheet.getCell(2, Math.max(columnCount - 2, 1)).font = { size: 11, color: { argb: "FF475569" } };
  worksheet.getCell(2, Math.max(columnCount - 2, 1)).alignment = { horizontal: "right", vertical: "middle" };
  worksheet.getRow(1).height = 28;
  worksheet.getRow(2).height = 22;
  worksheet.getRow(3).height = 10;

  const metaItems: ExcelMetaItem[] = [
    ...(!options.meta?.some(m => m.label === "Generated At") ? [{ label: "Generated At", value: formatDateTime(options.generatedAt) }] : []),
    ...(options.generatedBy && !options.meta?.some(m => m.label === "Generated By") ? [{ label: "Generated By", value: options.generatedBy }] : []),
    ...(!options.meta?.some(m => m.label === "Total Rows") ? [{ label: "Total Rows", value: options.rows.length }] : []),
    ...(options.meta || []),
  ];

  let rowIndex = 4;
  metaItems.forEach((item) => {
    worksheet.getCell(rowIndex, 1).value = item.label;
    worksheet.getCell(rowIndex, 1).font = { bold: true, color: { argb: "FF334155" } };
    worksheet.getCell(rowIndex, 1).fill = { type: "pattern", pattern: "solid", fgColor: { argb: "FFEAF0F7" } };
    worksheet.getCell(rowIndex, 2).value = toExcelCellValue(item.value);
    if (columnCount > 2) {
      worksheet.mergeCells(rowIndex, 2, rowIndex, columnCount);
    }
    rowIndex += 1;
  });

  rowIndex += 1;
  const headerRow = worksheet.getRow(rowIndex);
  columns.forEach((column, columnIndex) => {
    const cell = headerRow.getCell(columnIndex + 1);
    cell.value = column.title;
    cell.font = { bold: true, color: { argb: "FF0F172A" } };
    cell.fill = { type: "pattern", pattern: "solid", fgColor: { argb: "FFD9E2F3" } };
    cell.alignment = { horizontal: column.align || "left", vertical: "middle" };
    cell.border = {
      top: { style: "thin", color: { argb: "FFB7C4D4" } },
      left: { style: "thin", color: { argb: "FFB7C4D4" } },
      bottom: { style: "thin", color: { argb: "FFB7C4D4" } },
      right: { style: "thin", color: { argb: "FFB7C4D4" } },
    };
  });

  options.rows.forEach((row, dataIndex) => {
    const excelRow = worksheet.getRow(rowIndex + dataIndex + 1);
    columns.forEach((column, columnIndex) => {
      const rawValue = column.getValue(row, dataIndex);
      const cell = excelRow.getCell(columnIndex + 1);
      cell.value = toExcelCellValue(rawValue);
      cell.alignment = { horizontal: column.align || "left", vertical: "top", wrapText: true };
      if (typeof rawValue === "number") {
        cell.numFmt = "#,##0";
      }
      cell.border = {
        top: { style: "thin", color: { argb: "FFB7C4D4" } },
        left: { style: "thin", color: { argb: "FFB7C4D4" } },
        bottom: { style: "thin", color: { argb: "FFB7C4D4" } },
        right: { style: "thin", color: { argb: "FFB7C4D4" } },
      };
    });
  });

  worksheet.views = [{ state: "frozen", ySplit: rowIndex }];
  const buffer = await workbook.xlsx.writeBuffer();
  downloadWorkbookBuffer(buffer, options.fileName || buildExcelFileName(options.reportKey, options.generatedAt));
};

