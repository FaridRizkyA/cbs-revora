const getAssetUri = (asset: unknown) => {
  if (typeof asset === "number") {
    return Asset.fromModule(asset).uri || "";
  }

  if (asset && typeof asset === "object" && "uri" in asset && typeof (asset as { uri?: unknown }).uri === "string") {
    return (asset as { uri: string }).uri;
  }

  return "";
};

const blobToDataUri = (blob: Blob) =>
  new Promise<string>((resolve, reject) => {
    const reader = new FileReader();
    reader.onloadend = () => resolve(typeof reader.result === "string" ? reader.result : "");
    reader.onerror = () => reject(reader.error || new Error("Failed to read asset."));
    reader.readAsDataURL(blob);
  });

const assetToDataUri = async (asset: unknown) => {
  const uri = getAssetUri(asset);
  if (!uri) return "";
  if (uri.startsWith("data:")) return uri;

  if (Platform.OS !== "web") {
    return uri;
  }

  try {
    const response = await fetch(uri);
    if (!response.ok) return uri;
    const blob = await response.blob();
    return await blobToDataUri(blob);
  } catch {
    return uri;
  }
};

const assetToArrayBuffer = async (asset: unknown) => {
  const uri = getAssetUri(asset);
  if (!uri) return null;

  try {
    const response = await fetch(uri);
    if (!response.ok) return null;
    return await response.arrayBuffer();
  } catch {
    return null;
  }
};

const escapeHtml = (value: ExcelValue) =>
  String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");

const formatDateTime = (value?: string | Date | null) => {
  const date = value instanceof Date ? value : value ? new Date(value) : new Date();
  if (Number.isNaN(date.getTime())) return String(value || "-");

  return new Intl.DateTimeFormat("id-ID", {
    day: "2-digit",
    month: "2-digit",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit",
    second: "2-digit",
    hour12: false,
  })
    .format(date)
    .replace(/\./g, ":");
};

const formatValue = (value: ExcelValue) => {
  if (value instanceof Date) return formatDateTime(value);
  if (typeof value === "boolean") return value ? "Yes" : "No";
  if (value === null || value === undefined || value === "") return "-";
  return String(value);
};

const alignmentStyle = (align?: "left" | "center" | "right") => {
  if (align === "center") return "text-align:center;";
  if (align === "right") return "text-align:right;";
  return "text-align:left;";
};

const renderMetaRows = (items: ExcelMetaItem[], columnCount: number) => {
  if (items.length === 0) return "";

  return items
    .map(
      (item) => `
        <tr>
          <td class="meta-label">${escapeHtml(item.label)}</td>
          <td class="meta-value" colspan="${Math.max(columnCount - 1, 1)}">${escapeHtml(formatValue(item.value))}</td>
        </tr>
      `
    )
    .join("");
};

